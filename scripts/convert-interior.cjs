/**
 * Dev/authoring tool (not shipped at runtime): convert the Quaternius
 * "Ultimate House Interior" CC0 pack (OBJ+MTL, flat-colour low-poly) to GLB
 * under public/models/interior/, and emit catalog.json (id -> file, category,
 * bbox dims in source units). Re-run to regenerate.
 *
 *   node scripts/convert-interior.cjs
 *
 * Source: https://quaternius.com/  (CC0 1.0 — see PROVENANCE.md)
 */
const fs = require('fs');
const path = require('path');
const obj2gltf = require('obj2gltf');

const SRC = path.join(
  'assets',
  'Ultimate House Interior Pack',
  'Ultimate House Interior Pack - June 2020',
  'OBJ',
);
const OUT = path.join('public', 'models', 'interior');

/** Bounding-box size from the OBJ's `v x y z` vertex lines (source units). */
function objDims(objPath) {
  const min = [Infinity, Infinity, Infinity];
  const max = [-Infinity, -Infinity, -Infinity];
  for (const line of fs.readFileSync(objPath, 'utf8').split('\n')) {
    if (line[0] !== 'v' || line[1] !== ' ') continue;
    const p = line.slice(2).trim().split(/\s+/).map(Number);
    for (let i = 0; i < 3; i++) {
      if (p[i] < min[i]) min[i] = p[i];
      if (p[i] > max[i]) max[i] = p[i];
    }
  }
  const r = (n) => Math.round(n * 1000) / 1000;
  return { x: r(max[0] - min[0]), y: r(max[1] - min[1]), z: r(max[2] - min[2]) };
}

async function main() {
  fs.mkdirSync(OUT, { recursive: true });
  const objs = fs.readdirSync(SRC).filter((f) => f.toLowerCase().endsWith('.obj')).sort();
  const catalog = [];
  const failures = [];
  let n = 0;
  for (const file of objs) {
    const id = file.replace(/\.obj$/i, '');
    const category = id.split('_')[0];
    try {
      const glb = await obj2gltf(path.join(SRC, file), { binary: true });
      fs.writeFileSync(path.join(OUT, `${id}.glb`), glb);
      catalog.push({ id, file: `${id}.glb`, category, dims: objDims(path.join(SRC, file)) });
      if (++n % 20 === 0) console.log(`  ...${n}/${objs.length}`);
    } catch (e) {
      failures.push({ id, error: String(e && e.message ? e.message : e) });
    }
  }
  catalog.sort((a, b) => a.id.localeCompare(b.id));
  const DATA_TS = path.join('src', 'world', 'assets', 'catalog.data.ts');
  fs.mkdirSync(path.dirname(DATA_TS), { recursive: true });
  const header =
    '// GENERATED by scripts/convert-interior.cjs — do not edit by hand.\n' +
    '// Source: Quaternius "Ultimate House Interior" (CC0). See PROVENANCE.md.\n' +
    '// Re-generate: node scripts/convert-interior.cjs\n\n';
  fs.writeFileSync(DATA_TS, header + `export const INTERIOR_MODELS = ${JSON.stringify(catalog, null, 2)} as const;\n`);
  const cats = [...new Set(catalog.map((c) => c.category))].sort();
  console.log(`Converted ${catalog.length}/${objs.length} models -> ${OUT}`);
  console.log(`Categories (${cats.length}): ${cats.join(', ')}`);
  if (failures.length) {
    console.log(`FAILURES (${failures.length}):`);
    for (const f of failures) console.log(`  ${f.id}: ${f.error}`);
    process.exitCode = 1;
  }
}

main();
